        private uint F(uint x, uint y, uint z)
        {
            return (((x) & (y)) | ((~x) & (z)));
        }

        private uint G(uint x, uint y, uint z)
        {
            return (((x) & (z)) | ((y) & (~z)));
        }

        private uint H(uint x, uint y, uint z)
        {
            return ((x) ^ (y) ^ (z));
        }

        private uint I(uint x, uint y, uint z)
        {
            return ((y) ^ ((x) | (~z)));
        }

        private uint RL(uint x, int n)
        {
            return (((x) << (n)) | ((x) >> (32 - (n))));
        }

        private uint RR(uint x, int n)
        {
            return (((x) >> (n)) | ((x) << (32 - (n))));
        }

